public class J01_Simple_Function {

    public static void azim() {
        System.out.println("Hello, Azim!");
    }

    public static void main(String args[]) {
        azim(); // Calling
    }
}

// Functions are called Method in Java

// In Java, functions are referred to as "methods" because they are associated with a class or an object.
// In object-oriented programming, a function that is a member of a class is called a method.